# looping = we can use loop to iterate through
# the keys or values of a dic
d_1 = {1:"A", 2:"B"}
d_0 = {"Name":"Gokul", "Age":22}
# to print keys using for and key
for k in d_1.keys():
    print (k)
# to print values using for and values
for v in d_0.values():
    print(v)
# t0 print items in dic
for i,j in d_1.items():
    print(i,j)